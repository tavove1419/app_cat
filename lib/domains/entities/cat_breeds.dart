class CatBreeds {
    final String id;
    final String name;
    final String temperament;
    final String origin;
    final String description;
    final String lifeSpan;
    final int intelligence;
    final int adaptability;
    final String wikipediaUrl;
    final String referenceImageId;

    CatBreeds({
